def add(num_1, num_2):
    result = num_1 + num_2
    print(f'{num_1} + {num_2} is equal to {result}')
    return result

def subtract(num_1, num_2):
    result = num_1 - num_2
    print(f'{num_1} - {num_2} is equal to {result}')
    return result

def multiply(num_1, num_2):
    result = num_1 * num_2
    print(f'{num_1} x {num_2} is equal to {result}')
    return result

def division(num_1, num_2):
    result = num_1 / num_2
    print(f'{num_1} / {num_2} is equal to {result}')
    return result

def exponentiation(num_1, num_2):
    result = num_1 ** num_2
    print(f'{num_1}^{num_2} is equal to {result}')
    return result

def mod(num_1, num_2):
    result = num_1 % num_2
    print(f'{num_1} mod {num_2} is equal to {result}')
    return result